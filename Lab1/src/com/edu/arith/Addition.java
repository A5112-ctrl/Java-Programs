package com.edu.arith;

public class Addition {

	public static void main(String[] args) {

		int n1 = 10;
		int n2 = 20;
		int sum = n1 + n2;
		System.out.println("Addition: " + (sum));

		float n, m, s;// declaration
		n = 1.5f;// assigning values
		m = 2.5f;
		s = n + m;
		System.out.println("Sum : " + s);
	}

}
